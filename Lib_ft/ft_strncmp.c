int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	int i;

	i = 0;
	if (s1[0] == 0)
		return (-1 * s2[0]);
	while ((s1[i] == s2[i]) && (n > 0))
	{
		i++;
		n--;
	}
	return (s1[i] - s2[i]);
}
